// Generates src/generated/*.ts modules exporting third-party CSS as strings.
// tsdown 0.22 routes .css imports through its css-guard plugin (which wants
// CSS *bundling*), while we need CSS *text* to inject into a shadow root —
// so the strings are materialized as TypeScript ahead of the build.
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { createRequire } from 'node:module'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const require = createRequire(import.meta.url)
const rootDir = join(dirname(fileURLToPath(import.meta.url)), '..')
const outDir = join(rootDir, 'src', 'generated')

/** @type {Array<{ source: string; out: string }>} Sources starting with "./" are repo-local. */
const manifest = [
  { source: 'reveal.js/dist/reset.css', out: 'reveal-reset-css.ts' },
  { source: 'reveal.js/dist/reveal.css', out: 'reveal-css.ts' },
  { source: 'reveal.js/dist/theme/black.css', out: 'theme-black-css.ts' },
  { source: 'reveal.js/dist/theme/white.css', out: 'theme-white-css.ts' },
  { source: 'reveal.js/dist/theme/league.css', out: 'theme-league-css.ts' },
  { source: 'reveal.js/dist/theme/night.css', out: 'theme-night-css.ts' },
  { source: 'reveal.js/dist/theme/serif.css', out: 'theme-serif-css.ts' },
  { source: 'reveal.js/dist/theme/simple.css', out: 'theme-simple-css.ts' },
  { source: 'highlight.js/styles/github.css', out: 'hljs-github-css.ts' },
  { source: 'highlight.js/styles/github-dark.css', out: 'hljs-github-dark-css.ts' },
  { source: './src/themes/inkdrop.css', out: 'theme-inkdrop-css.ts' }
]

mkdirSync(outDir, { recursive: true })

for (const { source, out } of manifest) {
  const path = source.startsWith('./') ? join(rootDir, source) : require.resolve(source)
  const css = readFileSync(path, 'utf8')
  const module = [
    '// AUTO-GENERATED by scripts/gen-css.mjs — do not edit.',
    `// Source: ${source}`,
    `export default ${JSON.stringify(css)}`,
    ''
  ].join('\n')
  writeFileSync(join(outDir, out), module)
  console.log(`generated src/generated/${out} (${css.length} bytes) from ${source}`)
}
